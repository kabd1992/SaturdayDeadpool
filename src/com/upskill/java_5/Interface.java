package com.upskill.java_5;

public interface Interface {
	//Interface is to structure or enforce something .
	//Interface can have only abstract method, 
	//Abstract method doesn't have any implementation.
	
	public abstract void iDoor();
	public abstract void iSeat();
	public abstract void iWheel();
	public abstract void iRaduio();

}
/* (Parent) 		(Keyword) 		(Child)
 * Class			extends			Class
 * Abstract			extends			Class
 * Interface		Implements 		Interface
 * Interface 		extends 		Interface
 */
 
